class seven
{
    public static void main(String a[]) 
    {
        int num1 = 0b101; //binary
        int num2 = 0x7e; //e
        int num3 = 1_000_000; //you are allowed to use underscore here to keep track of zeros
        double num4 = 12e10;
        char c = 'a';
        c++; //iterate letters

        System.out.println("Welcome to Literals!");
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(c);

    }   
}
